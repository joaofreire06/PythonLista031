'''
 Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números
'''
#Etapa 1: entrada de dados
num1 = float(input("Informe a sua número 1: "))
num2 = float(input("Informe a sua número 2: "))
num3 = float(input("Informe a sua número 3: "))

#Etapa 2: Processamento de dados
média = (num1 + num2 + num3) / 3

#Etapa 3: Saída de dados (resposta na tela do usuário)
print("Sua média é", média)
